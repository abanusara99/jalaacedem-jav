package jala3;
// 7- get numbers from 1 to 10 with do while
public class dolop {
	public static void main(String[] args) {
        int number = 1;

        System.out.println("Numbers from 1 to 10:");

        do 
        {
            System.out.println(number);
            number++;
        } 
        while (number <= 10);
    }
}
